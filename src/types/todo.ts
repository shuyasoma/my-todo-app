/**
 * TODOアプリの型定義ファイル
 *
 * TypeScriptでは、データの形や構造を事前に定義することで、
 * コードの安全性を高めることができます。
 * このファイルでは、TODOアイテムがどのような情報を持つかを定義しています。
 */

// TODOアイテムの型定義
// interfaceは、オブジェクトの構造を定義するTypeScriptの機能です
// これにより、TODOアイテムが必ず以下の3つのプロパティを持つことが保証されます
export interface Todo {
  id: number; // 各TODOを識別するための一意の番号
  text: string; // TODOの内容（文字列）
  completed: boolean; // 完了しているかどうかの状態（true/false）
}

/**
 * 型定義のメリット：
 * 1. コードを書く時に間違いを防げる
 * 2. エディタが自動補完してくれる
 * 3. チーム開発で一貫性を保てる
 * 4. バグを事前に発見できる
 */